const svgCache = {};
const parser = new DOMParser();

function getSVG( name ) {

	if ( ! svgCache[ name ] ) {

		const svgString = SVG_ICONS[ name ];
		if ( svgString ) {

			const doc = parser.parseFromString( svgString, 'image/svg+xml' );
			svgCache[ name ] = doc.documentElement;

		}

	}

	return svgCache[ name ] ? svgCache[ name ].cloneNode( true ) : null;

}

async function compressString( str ) {

	if ( typeof CompressionStream === 'undefined' ) {

		return base64Encode( str );

	}

	try {

		const stream = new Blob( [ str ] ).stream();
		const compressedStream = stream.pipeThrough( new CompressionStream( 'deflate' ) );
		const response = new Response( compressedStream );
		const buffer = await response.arrayBuffer();

		const bytes = new Uint8Array( buffer );
		let binString = '';
		const chunkSize = 8192;
		for ( let i = 0; i < bytes.length; i += chunkSize ) {

			binString += String.fromCharCode.apply( null, bytes.subarray( i, i + chunkSize ) );

		}

		return btoa( binString );

	} catch ( err ) {

		return base64Encode( str );

	}

}

async function decompressString( base64 ) {

	if ( typeof DecompressionStream === 'undefined' ) {

		return base64Decode( base64 );

	}

	try {

		const binString = atob( base64 );
		const bytes = Uint8Array.from( binString, ( m ) => m.codePointAt( 0 ) );
		const stream = new Blob( [ bytes ] ).stream();
		const decompressedStream = stream.pipeThrough( new DecompressionStream( 'deflate' ) );
		const response = new Response( decompressedStream );
		return await response.text();

	} catch ( err ) {

		return base64Decode( base64 );

	}

}

function base64Encode( str ) {

	const bytes = new TextEncoder().encode( str );
	const binString = Array.from( bytes, ( byte ) => String.fromCodePoint( byte ) ).join( '' );
	return btoa( binString );

}

function base64Decode( base64 ) {

	const binString = atob( base64 );
	const bytes = Uint8Array.from( binString, ( m ) => m.codePointAt( 0 ) );
	return new TextDecoder().decode( bytes );

}

export { getSVG, compressString, decompressString, base64Encode, base64Decode };
